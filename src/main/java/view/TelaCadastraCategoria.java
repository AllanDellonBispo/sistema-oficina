    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (CampoCategoria.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo vazio.");
        } else {
            CategoriaController categoriaController = new CategoriaController();
            Categoria categoria = new Categoria();

            categoria.setNome(CampoCategoria.getText());
            categoriaController.adiciona(categoria);
            lerTabela();
            CampoCategoria.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        CategoriaController categoriaController = new CategoriaController();

             int confirma = JOptionPane.showConfirmDialog(null, "Essa categoria será removida, deseja continuar ?"
                  ,"Atenção",JOptionPane.YES_NO_OPTION);
          
          if(confirma == JOptionPane.YES_OPTION){
              
              try {
                categoriaController.remove(Integer.parseInt(CampoID.getValue().toString()));
                lerTabela();
              } catch (Exception e) {
                  JOptionPane.showMessageDialog(null, "A categoria em questão está associada a um insumo já cadastrado remova-o primeiro para continuar.");
              }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        CategoriaController categoriaController = new CategoriaController();
        Categoria categoria = new Categoria();

        try {
            categoria.setId(Integer.parseInt(CampoID.getValue().toString()));
            categoria.setNome(CampoNome.getText());
            categoriaController.atualiza(categoria);
            lerTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ID inexistente.");
        }
            
        
    }//GEN-LAST:event_jButton4ActionPerformed

