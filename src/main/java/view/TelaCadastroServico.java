    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        ServicoController servicoController = new ServicoController();
        Servico s = new Servico();

        if (CampoNome.getText().isEmpty() || CampoDescricao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para pode prosseguir.");
        } else {

            try {

                s.setNome(CampoNome.getText());
                s.setDescricao(CampoDescricao.getText());
                s.setPreco(Float.parseFloat(CampoPreco.getValue().toString()));
                s.setSolicitacoes(0);
                servicoController.adiciona(s);
                CampoNome.setText("");
                CampoDescricao.setText("");
                CampoPreco.setValue(0);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Utilize ponto(.) ao invés de virgula(,) para preencher o tempo caso seja um número decimal.");
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

