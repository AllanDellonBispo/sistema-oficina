    public void lerTabela(){
        
       FornecedorController f = new FornecedorController();
        
            
            DefaultTableModel modelo = (DefaultTableModel)Tabela.getModel();
            modelo.setNumRows(0);
            
            for(int i = 0; i < f.mostrarTodos().size(); i++){
            
                    modelo.addRow(new Object[]{
                    
                       f.mostrarTodos().get(i).getId(),
                       f.mostrarTodos().get(i).getNome(),
                       f.mostrarTodos().get(i).getCnpj(),
                       f.mostrarTodos().get(i).getEndereco(),
                       f.mostrarTodos().get(i).getTelefone(),
                    });
            }
    }
    
    
